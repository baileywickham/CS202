

public class GraphRange {

    public final double minX;
    public final double maxX;
    public final double minY;
    public final double maxY;

    public GraphRange(double minX, double maxX, double minY, double maxY) {
        this.minX = minX;
        this.maxX = maxX;
        this.minY = minY;
        this.maxY = maxY;
    }
}
